package practise;

public class DataTypes {
	public static void main(String[]args) {
		String firstname = "Apu";
		String lastname = "Ahmed";
		System.out.println(firstname +" "+ lastname); //use " " for make some space between first and last name
		
		//Byte - 8b it
		byte number = -128;
		byte number2 = 127;
		
		//short - 16 bit
		short x = -32768;
		short y = 32767;
		
		//int - 32 bit
		int a = -2147483648;
		int b = 2147483647;
		
		//long 64 bit l
		long z = 2144333333l;
		
		//double - decimal 64 bit d
		double f = 1.22d;
		
		//float - 32 bit f
		float g = 1.222f;
		
		//char - character - unicode - 16bit
		char h = '\u00e5';
		System.out.println(h);
		
		//Boolean - true / false
		boolean s = false;  
		
		
	}

}
